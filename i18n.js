const langNames = {
  zh: "中文", en: "English", fr: "Français", de: "Deutsch", es: "Español", it: "Italiano"
};

function detectLanguage() {
  const saved = localStorage.getItem("bp-lang");
  if (saved && translations[saved]) return saved;
  const nav = (navigator.language || navigator.userLanguage || "zh").toLowerCase();
  const exact = Object.keys(translations).find(k => nav === k || nav.startsWith(k + "-"));
  if (exact) return exact;
  return "zh";
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  const dict = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  if (dict["page.title"]) document.title = dict["page.title"];
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  localStorage.setItem("bp-lang", lang);
  // Update switcher display
  const display = document.querySelector(".lang-code");
  if (display) display.textContent = langNames[lang] || lang;
  // Update active state in dropdown
  document.querySelectorAll(".lang-dropdown button").forEach(btn => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });
}

function toggleLangMenu() {
  const dropdown = document.querySelector(".lang-dropdown");
  if (dropdown) dropdown.classList.toggle("open");
}

function initI18n() {
  // Setup dropdown button handlers
  document.querySelectorAll(".lang-dropdown button[data-lang]").forEach(btn => {
    btn.addEventListener("click", () => {
      setLanguage(btn.getAttribute("data-lang"));
      toggleLangMenu();
    });
  });
  // Close dropdown on outside click
  document.addEventListener("click", e => {
    const switcher = document.querySelector(".lang-switcher");
    if (switcher && !switcher.contains(e.target)) {
      const dropdown = document.querySelector(".lang-dropdown");
      if (dropdown) dropdown.classList.remove("open");
    }
  });
  // Apply detected language
  setLanguage(detectLanguage());
}

document.addEventListener("DOMContentLoaded", initI18n);
